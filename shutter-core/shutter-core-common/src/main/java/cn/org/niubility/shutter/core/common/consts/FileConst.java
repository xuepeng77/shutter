package cn.org.niubility.shutter.core.common.consts;

/**
 * 文件相关的常量。
 *
 * @author xuepeng
 */
public interface FileConst {

    /**
     * 文件后缀分割符。
     */
    String SEPRATOR_FILE_SUFFIX = ".";
    /**
     * 文件路径
     */
    String FILE_SPLIT = "/";
    /**
     * KB大小的文件。
     */
    String SIZE_KB = "KB";
    /**
     * MB大小的文件。
     */
    String SIZE_MB = "MB";
    /**
     * GB大小的文件。
     */
    String SIZE_GB = "GB";
    /**
     * 定义KB的计算常量。
     */
    int KB = 1024;
    /**
     * 定义MB的计算常量。
     */
    int MB = 1024 * 1024;
    /**
     * 定义GB的计算常量。
     */
    int GB = 1024 * 1024 * 1024;
    
}
